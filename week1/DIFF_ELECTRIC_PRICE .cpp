// CPP
#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cin >> n;
    double gia_de_xuat, gia_bac_6;
    if (n >= 0 && n <= 100)
    {
        gia_de_xuat = n * 1728;
        gia_bac_6 = (n <= 50) ? n * 1728 : 50 * 1728 + (n - 50) * 1786;
    }
    if (n >= 101 && n <= 200)
    {
        gia_bac_6 = 50 * 1728 + 50 * 1786 + (n - 100) * 2074;
        gia_de_xuat = 100 * 1728 + (n - 100) * 2074;
    }
    if (n >= 201 && n <= 400)
    {
        gia_de_xuat = 100 * 1728 + 100 * 2074 + (n - 200) * 2612;
        gia_bac_6 = (n <= 300) ? (50 * 1728 + 50 * 1786 + 100 * 2074 + (n - 200) * 2612) : (50 * 1728 + 50 * 1786 + 100 * 2074 + 100 * 2612 + (n - 300) * 2919);
    }
    if (n >= 401 && n <= 700)
    {
        gia_bac_6 = 50 * 1728 + 50 * 1786 + 100 * 2074 + 100 * 2612 + 100 * 2919 + (n - 400) * 3015;
        gia_de_xuat = 100 * 1728 + 100 * 2074 + 200 * 2612 + (n - 400) * 3111;
    }
    if (n >= 701)
    {
        gia_bac_6 = 50 * 1728 + 50 * 1786 + 100 * 2074 + 100 * 2612 + 100 * 2919 + (n - 400) * 3015;
        gia_de_xuat = 100 * 1728 + 100 * 2074 + 200 * 2612 + 300 * 3111 + (n - 700) * 3457;
    }
    cout << fixed << setprecision(2) << (gia_de_xuat - gia_bac_6) * 1.1;
}
